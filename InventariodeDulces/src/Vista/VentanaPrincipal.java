    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        
        dulces.barcode=Integer.parseInt(txtFieldbarcode.getText());
        dulces.nombproduct=txtFieldnombre.getText();
        dulces.pmayoreo=txtFieldmayoreo.getText();
        dulces.pindiv=txtFieldindividual.getText();
        dulces.undis=Integer.parseInt(txtFieldunidades.getText());
        dulces.marca=txtFieldmarca.getText();
        
        String vacio;
        vacio="";
         if((dulces.barcode == 0) && (dulces.nombproduct == vacio) && (dulces.pindiv == vacio )&& (dulces.pmayoreo == vacio) && (dulces.undis == 0) && (dulces.marca == vacio)){
        
                JOptionPane.showMessageDialog(this, "Asegure de llenar todos los campos");
        }
        else{
   
        
         if(dulces.barcode <100000000 && dulces.barcode>9999999){
           try{
             operaciones.GuardarRegistro(dulces); //envias todos los objetos pertenecientes
           } catch(Exception e){
               JOptionPane.showMessageDialog(this, "Asegurese de llenar todos los campos");
           }
            }
            else{
            JOptionPane.showMessageDialog(this, "Favor de indicar los 8 dÃ­gitos del cÃ³digo de barras");
            }
        }
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

